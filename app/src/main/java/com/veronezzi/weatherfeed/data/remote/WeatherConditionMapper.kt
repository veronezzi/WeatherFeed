package com.veronezzi.weatherfeed.data.remote

object WeatherConditionMapper {
    fun toEmoji(code: Int): String = when (code) {
        1000 -> "☀️"
        1003 -> "⛅"
        1006 -> "🌥️"
        1009 -> "☁️"
        1030 -> "🌫️"
        1063, 1180, 1183, 1186, 1189, 1192, 1195, 1240, 1243, 1246 -> "🌧️"
        1066, 1210, 1213, 1216, 1219, 1222, 1225, 1255, 1258 -> "❄️"
        1069, 1204, 1207, 1249, 1252 -> "🌨️"
        1072, 1150, 1153, 1168, 1171 -> "🌦️"
        1087, 1273, 1276, 1279, 1282 -> "⛈️"
        1114, 1117 -> "🌨️"
        1135, 1147 -> "🌫️"
        else -> "🌤️"
    }
}
